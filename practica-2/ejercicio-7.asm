LEER: PUSH AX
PUSH BX
PUSH CX

MOV BX, OFFSET MENSAJE1
MOV AL, OFFSET FINMENSAJE1 - OFFSET MENSAJE1
INT 7; IMPRIMO EL PROMPT
MOV BX, OFFSET NUM1
INT 6
MOV DH, [BX]
;YA LEI EL PRIMERO, ESTA EN DH
MOV BX, OFFSET MENSAJE2
MOV AL, OFFSET FINMENSAJE2 - OFFSET MENSAJE2
INT 7
MOV BX, OFFSET NUM2
INT 6
MOV DL, [BX]

POP CX
POP BX
POP AX
RET
ORG 2000H
CALL LEER
; ya tengo los numeros en DH y DL
SUB DH, 30H
SUB DL, 30H
MOV CL, DL
ADD CL, DH
; resultado en CL
CMP CL, 9H
JNS MAYOR_A_DIEZ
;si es menor a 10
