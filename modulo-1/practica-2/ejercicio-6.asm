ORG 3000H
IMPRIMIR_NUMERO: PUSH AX; PASAJE POR DL
PUSH BX
PUSH CX

MOV BX, OFFSET UNO
MOV AL, 6
MOV CL, 031H
LAZO: CMP DL, CL
JZ IMPRIMIR
INC CL
ADD BX, 6
DEC CANTIDAD
JZ FIN
JMP LAZO

IMPRIMIR: INT 7
FIN: POP CX
POP BX
POP AX
RET

