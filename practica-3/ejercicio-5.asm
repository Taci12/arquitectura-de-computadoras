PA EQU 30H
PB EQU 31H
CA EQU 32H
CB EQU 33H

ORG 1000H
CADENA DB ?
DB ?
DB ?
DB ?
DB ?
FIN_CADENA DB ?

ORG 3000H
STROBE: PUSH AX
IN AL, PA
OR AL, 00000010B
OUT PA, AL
POP AX
RET

IMPRIMIR: PUSH AX 
POLLING: IN AL, PA
AND AL, 1
JNZ POLLING
MOV AL, [BX]
OUT PB, AL
CALL STROBE
RET

ORG 2000H
MOV AL, 0
OUT CB, AL; todos en salida
MOV AL, 11111101b
OUT CA, AL; estado en salida menos el bit de strobe 
MOV BX, OFFSET CADENA
MOV CX, OFFSET FIN_CADENA
LOOP: INT 6
CALL IMPRIMIR
INC BX
CMP BX, CX
JNZ LOOP

END