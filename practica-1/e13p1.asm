ORG 3000h

ES_VOCAL: PUSH AX; DIRECCION DE CARACTER POR DX
PUSH BX; RESULTADO POR DL
PUSH CX

MOV CX, OFFSET VOCALES
MOV BX, DX
MOV AL, [BX]; LETRA EN AL
MOV BX, CX; OFFSET VOCALES EN BX
MOV AH, OFFSET CORTE - OFFSET VOCALES; AH CANTIDAD DE COMPARACIONES

LAZO: CMP [BX], AL
JZ FINTRUE
DEC AH
JZ FINFALSE
INC BX
JMP LAZO

FINFALSE: MOV DL, 000H
POP CX
POP BX
POP AX
RET

FINTRUE: MOV DL, 0FFH
POP CX
POP BX
POP AX
RET

ORG 2000H

MOV DX, OFFSET LETRA
CALL ES_VOCAL

HLT
END










